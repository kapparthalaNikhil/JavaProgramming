public class IndexValue {
    public static void main(String[]args){
        String names[]={"jadeja","watson","dhoni","raina","pathirana","rayudu","Rituraj","harbajanSingh","DeepakChahar"};
        for(int i=0;i<names.length;i++){
            System.out.println("Array element at index names["+i+"] is "+names[i]);
            i=i+2;
        }
        
    }
}
